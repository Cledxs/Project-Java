    private int centesimasSegundo=0,segundos=0,minutos=0,horas=0;
    
    private ActionListener acciones = new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
           centesimasSegundo++;
           if(centesimasSegundo==100){
               segundos++;
               centesimasSegundo = 0;
           }
           if(segundos ==60){
               minutos++;
               segundos=0;               
           }
           if(minutos==60){
               horas++;
               minutos=0;
           }
          actualizaretiquetatiempo();
        }
    };
    
    private void actualizaretiquetatiempo(){
        String texto = (horas<=9?"0":"")+horas +":"+ (minutos<=9?"0":"")+minutos +":"+ (segundos<=9?"0":"")+segundos+":" + (centesimasSegundo<=9?"0":"")+centesimasSegundo;
        etiquetatiempo.setText(texto);
    }
    
